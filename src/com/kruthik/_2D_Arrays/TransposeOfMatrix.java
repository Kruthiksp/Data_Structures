package com.kruthik._2D_Arrays;

public class TransposeOfMatrix {
    public static void main(String[] args) {
        int[][] arr = {{13, 21, 19, 48}, {44, 35, 36, 15}, {57, 28, 97, 32}, {71, 63, 88, 17}};

        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < arr[i].length; j++) {
                System.out.print(arr[i][j] + " ");
            }
            System.out.println();
        }
        System.out.println();
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < i; j++) {
                int temp = arr[i][j];
                arr[i][j] = arr[j][i];
                arr[j][i] = temp;
            }
        }
        for (int[] i : arr) {
            for (int j : i) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
